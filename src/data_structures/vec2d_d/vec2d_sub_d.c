#include "vec2d_sub_d.h"

t_s_vec2d_d	*vec2d_sub_d(t_s_vec2d_d *dest, t_s_vec2d_d *source)
{
	dest->x -= source->x;
	dest->y -= source->y;
	return (dest);
}
